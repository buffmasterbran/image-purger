// Helper function to check if a SKU is valid
export function isValidSKU(sku: string): boolean {
  if (VALID_SKUS.length === 0) {
    // If no SKUs are defined, allow all (for initial setup)
    return true
  }
  return VALID_SKUS.includes(sku.trim().toUpperCase())
}

// Helper function to get similar SKUs (for suggestions)
export function getSimilarSKUs(sku: string, maxResults: number = 5): string[] {
  const upperSku = sku.trim().toUpperCase()
  if (VALID_SKUS.length === 0) return []
  
  return VALID_SKUS
    .filter(validSku => {
      const upperValid = validSku.toUpperCase()
      return upperValid.includes(upperSku) || upperSku.includes(upperValid)
    })
    .slice(0, maxResults)
}
